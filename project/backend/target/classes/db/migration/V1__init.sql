CREATE EXTENSION IF NOT EXISTS citext;

-- ────────────────────────────────────────────────
-- USERS
-- ────────────────────────────────────────────────
CREATE TABLE users (
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    first_name         VARCHAR(255)    NOT NULL,
    last_name          VARCHAR(255)    NOT NULL,
    email              CITEXT          NOT NULL UNIQUE,
    phone_number       VARCHAR(50)     UNIQUE,
    password_hash      VARCHAR(255)    NOT NULL,
    status             VARCHAR(50)     NOT NULL DEFAULT 'ACTIVE',
    two_factor_enabled BOOLEAN         NOT NULL DEFAULT FALSE,
    two_factor_secret  VARCHAR(255),
    email_verified_at  TIMESTAMPTZ,
    phone_verified_at  TIMESTAMPTZ,
    last_login_at      TIMESTAMPTZ,
    last_login_ip      VARCHAR(45),
    created_at         TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    updated_at         TIMESTAMPTZ,
    deleted_at         TIMESTAMPTZ
);

-- ────────────────────────────────────────────────
-- REFRESH TOKENS (Stateless JWT Persistence)
-- ────────────────────────────────────────────────
CREATE TABLE refresh_tokens (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     BIGINT       NOT NULL,
    token_hash  VARCHAR(512) NOT NULL UNIQUE,
    expires_at  TIMESTAMPTZ  NOT NULL,
    revoked_at  TIMESTAMPTZ,
    device_info VARCHAR(255),
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),

    CONSTRAINT fk_refresh_tokens_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE
);

CREATE INDEX idx_refresh_tokens_user ON refresh_tokens (user_id);

-- ────────────────────────────────────────────────
-- AUDIT LOGS (Security Tracking)
-- ────────────────────────────────────────────────
CREATE TABLE audit_logs (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     BIGINT,
    action      VARCHAR(100) NOT NULL,
    entity_type VARCHAR(100),
    entity_id   BIGINT,
    ip_address  VARCHAR(45),
    user_agent  VARCHAR(512),
    details     JSONB,
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),

    CONSTRAINT fk_audit_logs_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE SET NULL
);

CREATE INDEX idx_audit_logs_user ON audit_logs (user_id);
CREATE INDEX idx_audit_logs_action ON audit_logs (action);

-- ────────────────────────────────────────────────
-- KYC VERIFICATIONS
-- ────────────────────────────────────────────────
CREATE TABLE kyc_verifications (
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id            BIGINT       NOT NULL UNIQUE,
    document_type      VARCHAR(50)  NOT NULL,
    document_front_url VARCHAR(512) NOT NULL,
    document_back_url  VARCHAR(512),
    selfie_url         VARCHAR(512),
    document_number    VARCHAR(255),
    status             VARCHAR(50)  NOT NULL DEFAULT 'PENDING',
    rejection_reason   TEXT,
    verified_at        TIMESTAMPTZ,
    created_at         TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at         TIMESTAMPTZ,

    CONSTRAINT fk_kyc_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE
);

-- ────────────────────────────────────────────────
-- BUSINESSES
-- ────────────────────────────────────────────────
CREATE TABLE businesses (
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id                 BIGINT          NOT NULL UNIQUE,
    name                    VARCHAR(255)    NOT NULL,
    currency                VARCHAR(3)      NOT NULL DEFAULT 'USD',
    country                 VARCHAR(100),
    timezone                VARCHAR(50),
    fiscal_year_start_month INTEGER         NOT NULL DEFAULT 1,
    status                  VARCHAR(50)     NOT NULL DEFAULT 'ACTIVE',
    created_at              TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    updated_at              TIMESTAMPTZ,
    deleted_at              TIMESTAMPTZ,

    CONSTRAINT fk_businesses_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE
);
